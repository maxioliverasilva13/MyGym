    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        boolean error = false;
        // Control de campo NOMBRE vacío.
        
        if (txtNombre.getText().equals("")){
            txtNombre.setBorder(javax.swing.BorderFactory.createMatteBorder(0, 0, 1, 0, new java.awt.Color(255, 0, 0)));
            error=true;
        }

        if (!error) {
            try {
                DtCategoria cat = new DtCategoria(txtNombre.getText());
                catBo.agregarCategoria(cat);
                JOptionPane.showMessageDialog(new JFrame(),  "Categoría insertada con éxito.", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(new JFrame(), e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }       
    }//GEN-LAST:event_btnAgregarActionPerformed

