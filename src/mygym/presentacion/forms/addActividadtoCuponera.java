    private void tablaInstitucionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaInstitucionesMouseClicked
        // TODO add your handling code here:
        int selectedRowId = tablaInstituciones.getSelectedRow();
        if(selectedRowId == -1){
            JOptionPane.showMessageDialog(new JFrame(), "Error, seleccione una instituci√≥n existente.", "Error", JOptionPane.ERROR_MESSAGE);
            cmbActividades.removeAllItems();
            return;
        }
        Object idObj = tablaInstituciones.getValueAt(selectedRowId, 0);
        int selectedInsitucionID = (Integer) idObj;
        cmbActividades.removeAllItems(); // Primero elimino todos los elems del combobox, para que solo se vean los de la instituci√≥n seleccionada en el grid.
        //System.out.println("Id de la institucion seleccionada en la tabla: " + selectedInsitucionID);
        try {
            HashMap<Integer, ActividadDTO> actividades = actBO.listarByInstitucionNotInCuponeras(selectedInsitucionID, idCupSeleccionada,"Aceptada");
            actividades.forEach((key, value) -> {
                ActividadDTO currentActividadDTO = actividades.get(key);
                model.addElement(new ComboItem(key.toString(), currentActividadDTO.getNombre()));
            });
            cmbActividades.setModel(model);
            cmbActividades.setSelectedItem(null);
        } catch (Exception e) {
            cmbActividades.setSelectedItem(null);
            JOptionPane.showMessageDialog(new JFrame(), e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }  
    }//GEN-LAST:event_tablaInstitucionesMouseClicked

    
    
